using System;

namespace CuentaRegresivaFor_g2
{
    class Program
    {
        static void Main(string[] args)
        {
            int n;
            //Inicialización; Condición; El Paso de Avance
            for(n = 10; n >= 0; n = n - 1)
            {
                Console.WriteLine(n);
            }
            Console.WriteLine("Feliz Año Nuevo");
        }
    }
}
