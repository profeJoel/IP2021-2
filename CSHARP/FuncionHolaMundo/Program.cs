            Console.WriteLine("Hola Mundo en Función");
        }

        static void Main(string[] args)
        {
            //Console.WriteLine("Hello World!");
            //Llamada a función HolaMundo
            HolaMundo(); // le digo al programa que ejecute la función HolaMundo
        }
    }
}
